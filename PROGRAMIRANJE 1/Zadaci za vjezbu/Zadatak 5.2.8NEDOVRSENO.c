/* Napisati program koji ispisuje cijele brojeve a i b te zatim ispisuje sve kubove brojeva izmedu njih. */
#include<stdio.h>
int main()
{
    int a, b;
    printf("Unesite cijeli broj a: ");
    scanf("%d", &a);

    printf("Unesite cijeli broj b: ");
    scanf("%d", &b);

    printf("Broj a = %d a njegov kub je %d\n", a, a*a*a);
    printf("Broj b = %d a njegov kub je %d\n", b, b*b*b);

return 0;
}


